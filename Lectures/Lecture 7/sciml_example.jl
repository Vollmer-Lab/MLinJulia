	_accuracy(x, y) = Flux.onecold(neural_net(x), 0:1) == y
	accuracy(x, y) = mean(_accuracy.(x, y))
end

# ‚ïî‚ïê‚ï° b4d0c632-65d7-4a82-871e-47c3cf2112ed
accuracy(train_x, train_y), accuracy(val_x, val_y)

