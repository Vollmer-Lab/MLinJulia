# ‚ïî‚ïê‚ï° 1bfc78a9-ea0c-4cfc-a869-1aefec5390a5
begin
	function trueODEfunc(du,u,p,t)
	    true_A = [-0.1 -2.0
	               2.0 -0.1]
	    du .= true_A * u.^3
	end
	
	u0_2 = Float32[2.; 0.]
	datasize = 30
	tspan2 = (0.0f0, 1.5f0)
	
	t = range(tspan2[1],tspan2[2],length=datasize)
	ode2_prob = ODEProblem(trueODEfunc,u0_2,tspan2)
	ode2_sol = solve(ode2_prob,Tsit5(),saveat=t)
	ode2_data = Array(ode2_sol)
end

# ‚ïî‚ïê‚ï° a691db51-9fc1-4f21-805e-4b3c34dcf263
md"""
Model $u'$ with neural network. (multilayer perceptron with 1 hidden layer and a tanh activation function)
"""

# ‚ïî‚ïê‚ï° b122e7e9-1113-4263-9040-7d6762ac78ee
begin
	dudt = Flux.Chain(
	    x -> x.^3,
	    Flux.Dense(2, 50, tanh),
	    Flux.Dense(50, 2),
	)
	
	n_ode = DiffEqFlux.NeuralODE(dudt, tspan2, Tsit5(), saveat=t,
	                  reltol=1e-7, abstol=1e-9)
	ps = Flux.params(n_ode)
	
	# Get the prediction using the correct initial condition
	pred = n_ode(u0_2);
	loss_n_ode() = sum(abs2, ode2_data .- n_ode(u0_2))
