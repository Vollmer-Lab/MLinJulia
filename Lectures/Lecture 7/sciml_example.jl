	    pl = scatter(t, ode2_data[1,:], label="data")
	    scatter!(pl, t, cur_pred[1,:], label="prediction")
	    plot(pl)
	end
	
	# Display the ODE with the initial parameter values.
	cbz()
	Flux.train!(loss_n_ode, ps, dataz, optz, cb = cbz)
end

