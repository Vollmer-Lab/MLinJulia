	params_ = Flux.params(p)
	function predict_rd() # Our 1-layer "neural network"
	    # override with new parameters
	    solve(ode_prob, Tsit5(), p=p, saveat=0.1)[1,:]
	end
	loss_rd() = sum(abs2, x-1 for x in predict_rd())
	
	
